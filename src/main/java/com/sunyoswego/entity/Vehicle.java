package com.sunyoswego.entity;

public class Vehicle {
	private int id;
	private String vid;
	private String tmstmp;
	private Double lat;
	private Double lon;
	private String hdg;
	private int pid;
	private String rt;
	private String des;
	private int dly;
	private int pdist;
	private int spd;
	private String tatripid;
	private String tablockid;
	private String zone;
	
	public Vehicle()
	{
		
	}

	public Integer getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getVid() {
		return vid;
	}

	public void setVid(String vid) {
		this.vid = vid;
	}

	public String getTmstmp() {
		return tmstmp;
	}

	public void setTmstmp(String tmstmp) {
		this.tmstmp = tmstmp;
	}

	public Double getLat() {
		return lat;
	}

	public void setLat(Double lat) {
		this.lat = lat;
	}

	public Double getLon() {
		return lon;
	}

	public void setLon(Double lon) {
		this.lon = lon;
	}

	public String getHdg() {
		return hdg;
	}

	public void setHdg(String hdg) {
		this.hdg = hdg;
	}

	public Integer getPid() {
		return pid;
	}

	public void setPid(int pid) {
		this.pid = pid;
	}

	public String getRt() {
		return rt;
	}

	public void setRt(String rt) {
		this.rt = rt;
	}

	public String getDes() {
		return des;
	}

	public void setDes(String des) {
		this.des = des;
	}

	public Integer getDly() {
		return dly;
	}

	public void setDly(int dly) {
		this.dly = dly;
	}

	public Integer getPdist() {
		return pdist;
	}

	public void setPdist(int pdist) {
		this.pdist = pdist;
	}

	public Integer getSpd() {
		return spd;
	}

	public void setSpd(int spd) {
		this.spd = spd;
	}

	public String getTatripid() {
		return tatripid;
	}

	public void setTatripid(String tatripid) {
		this.tatripid = tatripid;
	}

	public String getTablockid() {
		return tablockid;
	}

	public void setTablockid(String tablockid) {
		this.tablockid = tablockid;
	}

	public String getZone() {
		return zone;
	}

	public void setZone(String zone) {
		this.zone = zone;
	}
	

}
